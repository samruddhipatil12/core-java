package JungleBookExtends;

public class Animal {
	
String name ;
	
	public void hunt()
	{
		System.out.println(name+ " is hunting");
	}
	public void walk()
	{
		System.out.println(name+ " is walking");
	}
	public void eat()
	{
		System.out.println(name+ " is eating ");
	}
	public void sleep()
	{
		System.out.println(name+ " is sleeping ");
	}

}
